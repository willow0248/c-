/*我国从2021年元月起，执行新的个人所得税征收办法，按纳税年度合并计算个人所得税，如全年收入不超过6万元，应缴税率0%；如超过6万元，应先减去6万元，再按照下面的超额累进税率进行计算：

不超过36000元的部分,应缴税率3%；
超过36000元至144000元的部分，应缴税率10%；
超过144000元至300000元的部分，应缴税率20%；
超过300000元至420000元的部分，应缴税率25%；
超过420000元至660000元的部分，应缴税率30%；
超过660000元至960000元的部分，应缴税率35%；
超过960000的部分，应缴税率45%。
输入格式
一个正整数n（0≤n≤10,000,000），表示全年收入

输出格式
一个正整数，表示应缴税款。如税款带有小数部分，则舍去小数部分。
输入样例
66000
输出样例
180*/

#include <stdio.h>

int main() {
    double n;
    scanf("%lf", &n);
    
    double taxable = n - 60000;  // 应纳税所得额
    double tax = 0;
    
    if (taxable <= 0) {
        tax = 0;
    } 
    else {
        // 按超额累进税率计算
        if (taxable > 960000) {
            tax += (taxable - 960000) * 0.45;
            taxable = 960000;
        }
        if (taxable > 660000) {
            tax += (taxable - 660000) * 0.35;
            taxable = 660000;
        }
        if (taxable > 420000) {
            tax += (taxable - 420000) * 0.30;
            taxable = 420000;
        }
        if (taxable > 300000) {
            tax += (taxable - 300000) * 0.25;
            taxable = 300000;
        }
        if (taxable > 144000) {
            tax += (taxable - 144000) * 0.20;
            taxable = 144000;
        }
        if (taxable > 36000) {
            tax += (taxable - 36000) * 0.10;
            taxable = 36000;
        }
        tax += taxable * 0.03;
    }
    
    printf("%.0lf", tax);
    return 0;
}